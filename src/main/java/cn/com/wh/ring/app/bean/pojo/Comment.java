package cn.com.wh.ring.app.bean.pojo;

import org.apache.ibatis.type.Alias;

import java.util.Date;

/**
 * Created by Hui on 2017/8/3.
 */
@Alias("Comment")
public class Comment {
    public static final byte HOST_POST = 1;//帖子回复

    private Long id;
    private Long hostId;
    private byte hostType;
    private Long userId;
    private String content;
    private int praiseNumber;
    private int criticizeNumber;
    private int reportNumber;
    private byte isDeleted;
    private Date creationTime;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getHostId() {
        return hostId;
    }

    public void setHostId(Long hostId) {
        this.hostId = hostId;
    }

    public byte getHostType() {
        return hostType;
    }

    public void setHostType(byte hostType) {
        this.hostType = hostType;
    }

    public Long getUserId() {
        return userId;
    }

    public void setUserId(Long userId) {
        this.userId = userId;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public int getPraiseNumber() {
        return praiseNumber;
    }

    public void setPraiseNumber(int praiseNumber) {
        this.praiseNumber = praiseNumber;
    }

    public int getCriticizeNumber() {
        return criticizeNumber;
    }

    public void setCriticizeNumber(int criticizeNumber) {
        this.criticizeNumber = criticizeNumber;
    }

    public int getReportNumber() {
        return reportNumber;
    }

    public void setReportNumber(int reportNumber) {
        this.reportNumber = reportNumber;
    }

    public byte getIsDeleted() {
        return isDeleted;
    }

    public void setIsDeleted(byte isDeleted) {
        this.isDeleted = isDeleted;
    }

    public Date getCreationTime() {
        return creationTime;
    }

    public void setCreationTime(Date creationTime) {
        this.creationTime = creationTime;
    }
}
